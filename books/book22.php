                  <h1>《搶救寂靜：一個野地錄音師的探索之旅》</h1>
                  <br><br><br>
                  <p style="margin-left:8vw;font-weight:bold;">范欽慧</p>
                  <br><br>
                  <p>「寂靜」，算不算一種聲音？如果有一天，「寂靜」像是瀕臨絕種的動物一樣，變成了珍稀的存在，你可以想像會如何嗎？</p>
                  <br><br>
                  <p>范欽慧，在自然中聆聽記錄了17年的野地錄音師，她的腦海中有一張屬於台灣的聲音地圖。她知道在什麼季節，或什麼樣的海拔高度會聆聽到哪些動物的聲音。在自然中，她感受到靜謐和諧的美好，但也意識到有多少聲音，在我們無感無知的狀況下已成絕響。</p>
                  <br><br>
                  <p>為搶救「寂靜」，多年來她走遍台灣，在各地錄下自然天籟，並一一造訪從各種領域探索自然聲景（natural soundscape）的科學家、藝術家、田野工作者，為聲景的變化與消失留下第一手見證。這趟尋聲之旅不只在台灣，還走向國際；不只在陸地上，甚至海平面下都有她的追尋。</p>
                  <br><br>
                  <p>這是一本具有國際視野，結合自然文學、科學報導，及充滿夢想的作品。不僅敘述了26個為「寂靜」努力的故事，為了讓讀者更能進入故事中的聲音風景，特別設計了27則線上聲音注釋，由作者親自錄音、旁白。此外，書末並附珍貴記錄的12段自然聲景原音CD，邀請讀者一起踏上這趟聆聽「寂靜」的旅程！</p>
                  <br><br>
                  <p style="font-size:8vw;font-weight:bold;">作者介紹</p>
                  <br><br>
                  <p>范欽慧，自然作家、廣播電視節目製作主持人、紀錄片編導、田野錄音師。因為喜歡傾聽鳥鳴而走進自然，長期用聲音記錄台灣、並致力發展土地的聽音美學。</p>
                  <br><br>
                  <p>政大新聞學系學士、美國雪城大學廣播電視電影系碩士。於1997年起在教育廣播電台製作主持「自然筆記」節目迄今，曾獲五座廣播金鐘獎及多次入圍。同時擔任大愛電視「愛悅讀」節目主持人，並於2015年創立「台灣聲景協會」。</p>
                  <br><br>
                  <p>著有《跟著節氣去旅行》《搶救寂靜》等七本創作。文字作品曾獲林榮三文學獎、陽明海運海洋文學獎、永續台灣報導獎、入圍金鼎獎、卓越新聞獎等。</p>
