                <br>
                  <h1>《荒野天堂：保護區生態重建的故事》</h1>
                  <br><br><br>
                  <p style="margin-left:8vw;font-weight:bold;">林心雅</p>
                  <br><br>
                  <p>美國是世上規模最大的自然保育體系：世界第一座國家公園黃石公園早在1872年就成立，全美境內有五百餘個野生動物保護區、三百多個國家公園與國家紀念地以及長達近20萬公里的海岸線，生態環境包括濕地、荒漠、苔原、海岸、森林等各種棲地，總面積達9400萬英畝，約占美國表土面積4％。這一切都要歸功於高瞻遠矚的決策者和無數專業人士、有志之士和義工們的努力。</p>
                  <br><br>
                  <p>本書介紹美國兩個起死回生的保護區：新墨西哥州的阿帕契之林國家野生動物保護區，以及佛羅里達州的大沼澤國家公園；前者如今已是全美前十名最熱門的賞鳥勝地，後者則是美國第二座被聯合國遴選的世界自然遺產。作者透過說故事的手法，完整呈現保護區的成立緣由，過去半世紀以來面臨的瀕危問題，如何重建、修復遭破壞的自然環境，進而締造令人刮目的亮麗成果。</p>
                  <br><br>
                  <p>兩個保護區都以賞鳥為主。鳥類對飲水和空氣品質的要求比人類還高，對環境變化更是敏感警覺，堪稱「環境品質的指標」、「生態改變的先知」。而經過生態重建，這兩地已成為無數野鳥棲息的天堂，不但數量增加，種類上亦見生物多樣性的成果。文中穿插描繪各種有趣的「鳥行為」，搭配精采圖片，期能對北美鳥類有初步認識，並與台灣七股的國寶級珍禽黑面琵鷺及生態保護區，互相參照。</p>
                  <br><br>
                  <p>生態關係彼此環環相扣，上游不當開發往往為下游帶來災難。台灣因一味追求經濟成長而導致過度開發，或陷入發展經濟還是保護環境的兩難。美國的保護區是如何面對這些錯綜複雜的問題？又如何進行拯救？他山之石可以攻錯，本書探討的種種面向，對如今台灣正面臨的國土重整、山林河川復育、生物多樣性、永續發展、生態旅遊及綠色經濟等問題，堪稱最佳的借鏡參考！</p>
                  <br><br>
                  <p style="font-size:8vw;font-weight:bold;">作者介紹</p>
                  <br><br>
                  <p>林心雅，台北人。大學熱中爬山攝影，戲稱自己畢業於台大登山社。賓州州立大學環境社會學碩士。1997年成立個人工作室，用文字與影像替大自然說話。在國內外數次發表與生態保育相關報導及攝影作品。喜歡去人跡罕至的地方。認為世上最珍貴的，不但用錢買不到，也無法用錢來衡量，譬如新鮮的空氣、乾淨的水、溫暖的冬陽與家人的愛。真正美麗的事物，要用心才看得見。一直很喜歡一句話："Live Your Dreams"──人可以兩袖清風，但不能沒有夢想。有了夢想更要勇敢去追尋，因為人生只有一次。著有《Only In Alaska──跟我去阿拉斯加》、《凝固的波浪──科羅拉多高原》、《優勝美地四季之歌》、《地圖會說話──不可不思議的GIS》、《來自大海的朋友：象鼻海豹》。</p>
                  <br><br>
                  <p>李文堯，台大地理系畢，賓州州立大學地理碩士。目前從事地理資訊系統的研發工作。喜歡爬山也喜歡旅行，但不喜歡去人多的地方，而偏愛去野生動物多的荒野。如果不在旅行，就在計畫下一次的旅行。熱愛自然生態攝影，從1994起，每年都探訪阿拉斯加；踏勘美國西南區科羅拉多高原無數次，特別喜愛高原上各種奇岩地形；拍攝加州的優勝美地國家公園迄今已逾15年。攝影作品散見於國內外的雜誌書籍。著有《地圖會說話──不可不思議的GIS》、《跟我去阿拉斯加》、《凝固的波浪──科羅拉多高原》、《優勝美地四季之歌》、《來自大海的朋友：象鼻海豹》。</p>
