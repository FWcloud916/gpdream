                  <h1>《田園之秋》</h1>
                  <br><br><br>
                  <p style="margin-left:8vw;font-weight:bold;">陳冠學</p>
                  <br><br>
                  <p>《田園之秋》是一部三冊的散文集套書，以日記方式寫成，分為《初秋》、《仲秋》、《晚秋》三篇。描述著作者歸隱田園之後的生活實錄，透過農村四周景物的描寫，充分反映台灣本土所縕藏豐富的美，是作者對樸實生活的緬懷，和對昔日農村自然之美的謳歌。同時也是一本台灣少見的博物志，因為作者是一位「每事問」的農夫，他將所見所聞的野生動植物、生態環境、氣象景觀、農村的生活習性等面貌呈現出來。</p>
                  <br><br>
                  <p>全書充滿了他對單純田園生活的熱愛，以細膩的觀察、凝鍊的文字，寫下人和自然之間的和諧，是作者對台灣田園生活的緬懷和讚歌，蘊含了許多人文的思考和觀照。初秋篇的九月七日，描述一場西北雨的開始到結束，從起雲、打雷、閃電、降雨到放晴的經過，正如一場大自然的戲劇饗宴──高潮起伏、結局圓滿。文中又以人的渺小，來托大自然力量的偉大，以欣賞的眼光來看待這個大自然的傑作。最後以貝多芬田園交響曲末樂章的牧羊人之歌來呈現雨過天晴的安詳、平和。這是知識份子重返自然、融於自然的真情結晶，它以樸拙凝練的田園日記式，描寫農家四周景物，充分反映台灣這塊美麗大地所孕育的內藏之美。</p>
                  <br><br>
                  <p style="font-size:8vw;font-weight:bold;">作者介紹</p>
                  <br><br>
                  <p>陳冠學，一九三四年出生，台灣屏東縣人。師大國文系畢業，曾任教師和編輯。具有中國傳統的舊文人氣質，同時又具有知識份子參與的入世思想。七○年代初，他毅然辭掉教職，重歸故鄉田園，晴耕雨讀，過清貧樂道的書生農夫的生活。</p>
                  <br><br>
                  <p>陳冠學曾精研中國古代思想，出版有「象形文字」「莊子新傳」「倫語新注」「莊子新注」「莊子」等，成就頗可觀。他退隱田園後，也專注於台灣拓荒歷史和台語的研究，著有「老台灣」「台語之古老與古典」，筆鋒常帶摯愛這塊土地的一股熱情，叫人掩卷而久久心情激動。</p>
