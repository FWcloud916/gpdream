                  <p>我們想像的未來，就是我們準備為下一代創造的未來。過去宜蘭拒絕了六輕，而麥寮接受了六輕。今天國光石化（八輕）的議題，既是經濟、生態、汙染，環境正義的議題，更是思考島嶼未來的議題。本書廣泛收錄學者、藝文創作者、新聞媒體工作者、獨立創作者對國光石化（八輕）建廠的反省與思考，蒐羅、呈現了各種意見與客觀論述。選擇不再旁觀，您可以從認股夕陽、溪口、溼地，白海豚開始，也可以從打開這本書開始。</p>
