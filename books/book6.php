                  <p style="font-size:8vw;font-weight:bold;">作者介紹</p>
                  <br><br>
                  <p>吳晟本名吳勝雄，台灣彰化縣溪州鄉圳寮村人，1944年生，屏東農業專科學校（現已改制為國立屏東科技大學）畢業，任彰化溪州國中生物科教師以迄退休，同時務農躬耕，服務桑梓，並從事詩及散文的創作，曾應邀參加美國愛荷華大學國際作家工作坊。</p>
                  <br><br>
                  <p>「寫台灣人、敘台灣事、繪台灣景、抒台灣情」是吳晟的創作主張。他的作品都是從生活體驗中醞釀出來，情境動人，包含深刻的哲理。著有詩集《飄搖裡》、 《吾鄉印象》、《向孩子說》、《吳晟詩選》，散文集《農婦》、《店仔頭》、《無悔》、《不如相忘》、《一首詩一個故事》、《筆記濁水溪》等。</p>
